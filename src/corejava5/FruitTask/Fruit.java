package corejava5.FruitTask;

public class Fruit {
        String color;
        double averageWeight;

        public Fruit(String color, double averageWeight) {
            this.color = color;
            this.averageWeight = averageWeight;
        }

        public String getColor() {
            return color;
        }

        public double getAverageWeight() {
            return averageWeight;
        }
    }
